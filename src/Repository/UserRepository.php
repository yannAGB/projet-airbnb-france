	public function supprimerTokensUtilisateur(User $user): void
	{
		$this->getEntityManager()
			->createQueryBuilder()
			->delete(AccessToken::class, 't')
			->where('t.user = :user')
			->setParameter('user', $user)
			->getQuery()
			->execute();
